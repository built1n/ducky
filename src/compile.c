/*******************************************************************************
 * Bytecode format (all numbers are little-endian):
 *
 * +0x00: 'DucK' signature (4 bytes)
 * +0x04: number of lines (4 bytes)
 * +0x08: line offsets (4 * num lines)
 * +    : compiler-generated init code
 * +(0x08): start of compiled code
 */

